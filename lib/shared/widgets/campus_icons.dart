// Extension pour faciliter l'utilisation des icônes
extension IconExtension on Icon {
  Icon get campus => Icon(
    icon,
    size: 24,
    color: CampusColors.gray600,
  );

  Icon campusSize(double size) => Icon(
    icon,
    size: size,
    color: CampusColors.gray600,
  );

  Icon campusColor(Color color) => Icon(
    icon,
    size: 24,
    color: color,
  );

  Icon campusSizeColor(double size, Color color) => Icon(
    icon,
    size: size,
    color: color,
  );
}

// Widget pour les icônes thématiques du campus
class CampusIcon extends StatelessWidget {
  final IconData icon;
  final double? size;
  final Color? color;
  final String? semanticLabel;

  const CampusIcon(
    this.icon, {
    super.key,
    this.size,
    this.color,
    this.semanticLabel,
  });

  @override
  Widget build(BuildContext context) {
    return Icon(
      icon,
      size: size ?? 24,
      color: color ?? CampusColors.gray600,
      semanticLabel: semanticLabel,
    );
  }
}

